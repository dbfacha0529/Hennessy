<section id="sec2">
  <h2>第2条（利用条件）</h2>
  <ol>
    <li>本アプリを利用できるのは、満18歳以上（高校生を除く）の者に限ります。</li>
    <li>利用者は、自己の責任で正しい情報を登録しなければなりません。虚偽の登録が判明した場合、以後の利用を制限します。</li>
    <li>当店は必要に応じて年齢確認や本人確認を求めることができ、利用者はこれに応じなければなりません。</li>
  </ol>
</section>

<section id="sec3">
  <h2>第3条（禁止行為）</h2>
  <p>利用者は以下の行為を禁止します。違反が判明した場合、予約取消・強制退会・違約金の請求・警察への通報を行う場合があります。</p>
  <ul>
    <li>18歳未満の利用。</li>
    <li>暴力団関係者、薬物使用者の利用。</li>
    <li>ストーカー行為、強要、脅迫、暴力。</li>
    <li>店舗・キャストの評判を毀損する行為（SNS等への誹謗中傷を含む）。</li>
    <li>無断キャンセル、虚偽予約。</li>
    <li><strong>本番行為（性交等）、およびそれを強要・要求する行為。</strong></li>
    <li><strong>当店を介さずにキャストと直接連絡・取引を行う行為（いわゆる「直引き」）。</strong></li>
    <li>その他、当店が不適切と判断する行為。</li>
  </ul>
</section>

<section id="sec4">
  <h2>第4条（予約・キャンセル）</h2>
  <ol>
    <li>予約は本アプリを通じて行うものとし、店舗が承認した時点で成立します。</li>
    <li>キャンセルは、利用開始時刻の1時間前までに行う場合に限り、キャンセル料は発生しません。</li>
    <li>利用開始時刻の1時間前を過ぎてのキャンセル、または無断キャンセルの場合は、利用料金の100％を違約金として請求します。</li>
  </ol>
</section>


<section id="sec5">
  <h2>第5条（料金・支払）</h2>
  <ol>
    <li>料金は当店が定める料金表によります。</li>
    <li>支払い方法は当店が指定する方法に従うものとします（現金・キャッシュレス等）。</li>
    <li>返金はいかなる場合も行いません（ただし、当店の責に帰すべき事由によりサービス提供が不能となった場合を除きます）。</li>
  </ol>
</section>

